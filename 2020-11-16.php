          <p>On top of the music and the lyrics, just look at him! He's got on his trademark balaclava and the sort of ornate jewelry that's commonplace among hip hop artists, but with what else? A cardigan, a flannel shirt, and ripped jeans. He's RMRing Matchbox Twenty, and he's Matchbox Twentying RMR. He's hip hopping rock music, and rocking hip hop music. He's putting two disparate, established things next to one another, placing them in juxtaposition, to put them in conversation with each other, and that conversation itself creates something new. This is Histoire(s) du Cinema-era Godard. It's Dylan circa 1965. ("[Time magazine] would go off the newstands in a week if they printed the real truth...a plan picture...a tramp vomiting, man, in the sewer. And next door to the picture, Mr. Rockefeller...just some kind of collage, which they don't do. There's no ideas at Time magazine.") This is the most unique, original, creative, and frankly brave pop artist we've seen in a very long time. Probably since the emergence of Kanye West.</p>
                      
