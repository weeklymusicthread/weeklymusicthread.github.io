          <p>Now, about those calls for unity. [Insert long paragraph lamenting increasing trends toward polarization, or whatever it is that people talk about.] There are a lot of people out there talking about how to fix all of this, and they've got a lot of ideas. But we've got an idea of our own.</p>

          <p>We won't discuss here how the election was subverted, the democracy was undermined, and the presidency was stolen from its rightful candidate, Kanye West. Instead, we'd like to talk about a different hip hop artist who we think can bring unity to this broken nation, and that's the 2020 breakout star RMR.</p>

          <p>Those of you who've been around here long enough know that we at the WMT think that RMR is the single most important development of 2020, bar none. It all started with the abrupt, unannounced, highly unanticipated music video for a track no one had ever heard by an artist no one even knew existed. On February 26th, this nobody released <a href="https://www.youtube.com/watch?v=WCdAtvwW1E8">"Rascal,"</a> which took both the internet and us by storm. We got it courtesy of Dr. Koller, MD--urologist to the stars--as we were pulling into work. We sat in our car and watched the video 3 or 4 times before sending it to just about everyone we knew. (And more on "Rascal" below.) Over the following weeks and months, we hung on every YouTube clip as RMR released more and more content, until eventually dropping his debut album, Drug Dealing is a Lost Art.</p>

          <p>What was it that made "Rascal" so special? Well, we partially feel that, if we could explain it entirely, it wouldn't actually have been so special. But there are elements that seem obvious. For one thing, the choice of sample stands out almost immediately. The track backing is taken from Rascal Flatts' "Bless the Broken Road," which is just about the farthest thing from trap music that there is. This makes it a superlatively bold choice for a debut in what is perhaps the single most conservative genre of popular music that exists today. And although RMR's take largely deals with his experiences as a drug dealer, it also does display a surprising amount of vulnerability (again, for a trap-inspired track), even if it is at times somewhat tongue-in-cheek. In addition to that, there's the imagery--personalized balaclavas, an apparently authentic(?) St. Laurent bulletproof vest, and a very large number of very real-looking firearms. The way that they are brandished, sexualized, and fetishized throughout the video pushes things from reification over into near parody. And then there are the visual effects. They seem almost intentionally crude, angular, blocky. In short, they are totally at odds with the hysterical obsession with image that pervades so much hip hop music. So what is going on here? Is this high brow, or low? Is it high fashion or trash? Is it meaningful or silly? Vulnerable or violent? Hip hop or country? And is it low budget, or high?</p>

          <p>That last point is an interesting one with respect to RMR. First of all, we imagine that the sample for "Bless the Broken Road" is costing him something, especially given how much play he's been getting, which suggests that someone is paying for it. But who? And who would place that bet on an artist with no track record? Even if the video for "Rascal" itself didn't suggest some degree of financial backing, subsequent videos for tracks like "Dealer" certainly did. Who is RMR, how did he get people behind him, and who are those people? Currently, no one has the answers to these questions.</p>

          <p>Well, now RMR is back. This past week, he released a video for <a href="https://www.youtube.com/watch?v=AvpRS18OTTk">"The Wishing Hour,"</a> which is an abbreviated, organic hip-hop re-imagining of Matchbox Twenty's "3 AM" (1996) as a lament not about loneliness, but about the frustrations which might beset one in a strip club. It seems impossible for us to overstate how great this is. Like "Rascal," "The Wishing Hour" seems at first glance to cheapen both the original as well as itself. There's no doubt that, especially when "3 AM" was released, the crude strip club imagery that RMR inserts here would have totally barred it from achieving the fame it did. There also seems little doubt that, prior to this week, any hip hop artist who dared to sample Matchbox Twenty would not have garnered much respect from the rest of the genre. Perhaps one of the only '90s acts which has aged worse than Matchbox Twenty is the Red Hot Chili Peppers, and you don't see many hip hop artists sampling them. (To be clear, Matchbox Twenty's music is actually pretty pleasant, and the early records have some pretty solid tracks on them. But that hasn't stopped them from becoming a bit of <a href="https://www.youtube.com/watch?v=CClYz4Km5es">a bit of a butt of the joke</a> in recent years.) Moreover, a lot of people tried to pigeonhole RMR into a "country trap artist" or some such on the heels of the success of "Rascal," but this is a move that we don't think anyone saw coming.</p>

          <p>Now, on top of the move itself, the execution is just perfect. For one thing, the extended open builds anticipation. For the first 30-45 seconds, you're lost. You're not sure where this is going, and whether or not you should be waiting for some sort of "beat" to "drop." Whoa, what's that--an actual, physical drum set? An acoustic guitar? And then those first chords. (This, by the way, is where the choice of "3 AM" starts to feel like real genius, for two reasons: 1) everyone recognizes those opening chords, but as they hang in the air during the extended rest 2) nobody listening in 2020 can quite place them.) Then, as the rest of the intro plays out, RMR gyrates around his mic stand and offers a demented smile to the camera as if to say that everything is a little demented here. And then you realize what they're playing, and what the title means. "3 AM"--or, the witching hour. The hour at which demons come out to play. And why? Because, as three is the holiest number, 3:00 is the time when malevolent forces ironically choose to inflict their worst insults against God and man. RMR is witching the Matchbox Twenty track itself, inverting it, sowing it with irony, turning it on its head, just like he does with genre, fashion, everything. And, just like "3 AM" is one small leap in logic away from "the witching hour," so too is "the witching hour" from "the wishing hour." And why else has he called it that?</p>

          <p>We find out almost immediately. This take on "3 AM" takes place in a strip club, and there really isn't any other way to interpret the lyrics. Now, at first, they feel like a rough approximation of the original, but if you look at the lines side by side, they're actually quite close:</p>

