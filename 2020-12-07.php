          <p>Well, friends and faithful readers, it's all coming to an end.</p>

          <p>The turn of the calendar from November to December marks the winding down of the year. [Insert obligatory self-aggrandizing comment about 2020 earning some superlative in history and in absolutely no way being as cosmically unremarkable as every other year that we--or anyone else--has ever experienced.] That being said, there was one thing that was special about 2020: the birth of the Weekly Music Thread. </p>
        
          <p>And born it was. It was born, and it grew, and it went forth and multiplied. But what of it? What did any of that mean? What was it really worth? What is birth if not the prelude to death? What is a beginning if not a spartan guarantee only of its opposite: an end? Is it simply the year that will end at midnight on December 31st, or something more?</p>
          
          <p>Do not be afraid. To paraphrase Angelfire homepages circa 2004, cry not because it's over, but smile because it happened. Alternatively, perhaps our readers prefer 1998 one-hit-wonder Semisonic: every new beginning comes from some other beginning's end. Prescient, isn't it? Because the truth is, folks, this is, in some ways, the beginning of the end of the Weekly Music Thread as you know it. (Of course, that being said, it's also, according to the same, aforementioned transitive property of "Closing Time," the beginning of the end <i>of the beginning</i> of the Weekly Music Thread as you know it...try to keep up.)</p>

          <p>What are we talking about? Nevermind that now (via Kirk Cobain). Because all you need to know for now is this: it's time to start compiling your year-end lists. Specifically, we're looking for two different lists from each of you--your 10 favorite songs of the year, and your 10 favorite albums of the year (In order, if rankings are your sort of thing.) Now, keep in mind that we here at the WMT choose our words wisely, which is why we said favorite and not best. We're not into joyless taxonomies and pseudoscientific 10.0 critical scales. These lists are just about making a record of the things from 2020 you never want to forget. Kinda like The Lincoln Project, but for heady nuggz.</p>
          
          <p>You've got the month to prepare your entries. Instructions to follow in the <b>Audience participation round</b> below.</p>
                    
