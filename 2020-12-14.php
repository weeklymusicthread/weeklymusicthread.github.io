          <h2 class="section-heading" style="font-size:125%">Up The Punx</h2>

          <p>This week, as we bring you another dubious selection for the genre and our punk credentials suffer even further, we present Bully's <i>SUGAREGG</i> (2020). For fans of Charly Bliss and Sincere Engineer, this kind of sounds like if those two bands met up and made a record about their bipolar disorder. (Maybe sprinkle a touch of White Lung in as well?) This is raw, messy stuff--the kind of record that really would've been ruined with over-production, but there's just enough distortion on the instrumentals and rust on Alicia Bognanno's voice to capture the sort of sentiments that the album's lyrics put forward.</p>

