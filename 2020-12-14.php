          <p>As we're sure you're all aware, the year is winding to a close. And, as such, we decided it would be a good idea to get our affairs in order. You know, taking out insurance policies, investing in Dow Jones' industrial assets, and swinging by our local Ed.D. for our annual physical exam.</p> 
          
          <p>It was in the pursuit of this lattermost endeavor that something compelling happened. And because compelling somethings so rarely happen--especially to us--we thought it fit to share with you, our loyal readers, here. We arrived at the DOCTOR's office (get used to it), took a number, and sat down. Minutes passed, a quarter hour, a half hour. Aside from the idle small talk that came along with every patient as they checked in, there was no conversation in the waiting room. Throughout this peaceful time, a man and a woman who were presumably married or otherwise together, sat silently about six feet across from us. (His name is DOCTOR Fauci--get used to it.) They hadn't spoken at all, neither to one another nor anyone else, and we hadn't noticed much about them. They had seemed unremarkable. As it turned out, they were not.</p>
          
          <p>That's because, after thirty minutes of idle silence and without even using a cell phone--really just absentmindedly staring at the floor--the man cleared his voice and spoke. "Seen that movie <i>Bubble Boy</i>," he offered, in an intersection of construction and delivery which felt perfectly ambiguous. Was this a question? A declaration? His tone gave away nothing, and neither did his demeanor. On top of that, the--comment? question? utterance?--seemed as though it had been directed toward nobody in particular. Certainly neither his companion nor any of the other patients in the waiting room so much as acknowledged that the interminable silence had just been broken (and by a comment about the 2001 Jake Gyllenhaal vehicle <i>Bubble Boy</i>--which did for severe combined immunodeficiency (or SCID) what <i>Philadelphia</i> did for HIV/AIDS--no less.)</p>
          
          <p>"Stoopit," the man went on, shaking his head in dismissive rebuke. "Corny as hell." We looked around. If anyone else was hearing this, they gave no indication of it.</p>
          
          <p>"Still," the man piped up again one final time. This time, he lifted his head to look around the room, one of his eyebrows raised. He showed neither the flat affect nor the naked condescension he had worn just moments earlier. His tone was now...elegiac. "He lived like that. That was how he lived." And with that, he returned his gaze to the floor and was silent again.</p>

          <p>We were flabbergasted. What could this have been? A film review? A commentary on the prescience of <i>Bubble Boy,</i> vis-a-vis the masking, hand sanitizing, social distancing fads that would come into fashion almost twenty years later? Are we all living like that? Is that how we're living? And was he protesting that? Was this man an anti-masker? Was he an anti-vaxxer? A science denier? A Tea Party-goer? An InfoWarrior? A Proud and/or Boogaloo Bo(y/i)? Was this a Q drop? We had so many questions, but looking at the man then, it was clear that he had nothing more to offer. He had said his piece, and had returned to his resting state, his own sort of reactionary Bubble Boy. God bless.</p>

