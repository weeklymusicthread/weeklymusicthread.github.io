          <h2 class="section-heading" style="font-size:125%">From the Vault</h2>

          <p>Taylor Swift released a new record. Although we haven't had time to listen to it, we understand that it inhabits the same, uncapitalized universe as folklore. That, plus the Dylan, got us listening to some other folk-adjacent music, including The Tallest Man on Earth. We've been revisiting <i>The Wild Hunt</i> (2010) as well as several tracks off of <i>Dark Bird is Home</i> (2016), most notably "Slow Dance." Although Kristian Matsson is an excellent guitar player--he experiments with lots of alternative tunings and makes severely complicated fingerpicking parts look easy--his real allure lies in two areas: 1) his roughly hewn voice, and 2) the poetry of his lyrics. First, the voice. It's not pretty, which is kind of what makes it, paradoxically, so welcoming. When he sings softly, he sounds almost like someone you know. And when he belts, you really feel all of the strain, which comes as a sort of proxy for the emotion you imagine he's feeling. Now, a lot of folk music is essentially just pleasant melodies which allow the lyrics to do the heavy lifting, and rather than saying that this is no different, we're going to say that this is actually an exalted example of that. When Matsson sings things like "Oh, but rumor has it that I wasn't born/I just walked in one frosty morn/Into the vision of some vacant mind," we feel as though, well, neither were we born. (And, in some sense, this is true, isn't it? No one, after all, can access the experience of their own birth.) Or on the aforementioned "Slow Dance," in which Matsson laments his life on the road and what it is doing to his marriage back home, he sings "And now I hold on fall is greeting me to stay/And we slow dance in the kitchen and I dream the days away, oh" so swiftly that you almost miss it. But after the first time you do notice it, you can never listen to the song again without feeling fully that line's deep poignancy. (And if that hits hard, just wait until the final verse: "In this sunlight I just can't believe your face/We're some laundry line believers/We're just kids in many ways, oh/And you smell like smoke and honey in my arms/I'm somewhere in some falling and I'm learning of your language for to say/In times like these I should never grow away." The language is so simple, and yet (or perhaps it's 'and so'?) it captures so effectively such a complicated mixture of love, longing, guilt, regret--and all with meter and rhyme, from someone who isn't even a native English speaker. Eat your heart out, Louise Gluck.)</p>

