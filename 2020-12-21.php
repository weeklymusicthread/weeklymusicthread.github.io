          <p>Now, one might think that this would not inspire confidence. No one looks good here--neither the intern who wrote this list, nor the former hope/change enthusiast who selected that intern for the job. And yet, the community seemed to accept it without much of a fight. Perhaps that's because Obama as film reviewer doesn't necessarily play fair. (If your friendly neighborhood WMT winds up, as a result of this whisteblowing, prosecuted under the 1917 Espionage Act, you'll know why!) At any rate, we'll wait to see what next year brings. (If we were betting men, we'd put our money on a list that starts with a movie whose title is composed exclusively of special characters, then includes a couple of titles made up of numerals, and ends with a film called <i>Aardvark</i>. Or maybe just <i>Aaaaaaaaa</i>. There are gonna be a lot of 2020 retrospectives to fit in the middle of that list, after all.)</p>

