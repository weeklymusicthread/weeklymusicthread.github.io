          <blockquote class="blockquote">"It's the end of the world as we know it, and I feel fine" - Greta Thunberg </blockquote>

          <p>We were reminded last night of an interview with Colin Firth (<i>In Bruges, The Lobster</i>) in which he said that "whenever you take on playing a villain, he has to cease to be a villain to you. If you judge this man by his time, he's doing very little wrong." Transphobic, cisheteronormative language notwithstanding, this basically means that, like, "I see where he (villain) is coming from."</p> 
         
          <p>The quote came back to us as, in preparation for <i>Wonder Woman 1984</i> (aka WW84), we watched the original Gal Gadot <i>Wonder Woman</i> (2017). In that film, the villain is Ares (sort of--you'll see if you watch it), the God of War, who seeks to destroy humanity due to some combination of its general unworthiness as a creation of Zeus and an ecoterrorist revenge plot in response to their poor stewardship of the planet. Gee, where have we heard that before?</p>

          <p>If you think about it, every villain from every movie from the past 5-10 years has been an anti-humanist and at least implicit (often actually explicit) ecoterrorist. Some additional examples:</p>

          <p style = "padding-left: 2em">- Thanos in...<i>Avengers</i> (? I don't actually know what these movies are called)</p>

          <p style = "padding-left: 4em">Thanos is an anti-humanist ecoterrorist who seeks to bring "balance" to the universe by wearing a special (in all likelihood biodegradable) glove that allows him to snap his fingers and prevent overpopulation by killing off half of the universe's living creatures through Green New Deal policies. Thanos: "The universe is finite, its resources, finite. If life is left unchecked, life will cease to exist." (Evidently, Thanos does not follow Neil MiddleName Tyson on Twitter.)</p>

          <p style = "padding-left: 4em">So, how did critics respond?</p>

          <p style = "padding-left: 4em"><a href="https://www.forbes.com/sites/jvchamary/2019/05/07/avengers-endgame-biodiversity/?sh=aa82c29775b4">"The Science of 'Avengers: Endgame' Proves Thanos Did Nothing Wrong"</a></p>

          <p style = "padding-left: 4em"><a href="https://movieweb.com/thanos-infinity-war-main-hero-marvel-movie-universe/">"Why Thanos is Actually the Hero of the MCU"</a></p>

          <p style = "padding-left: 4em"><a href="https://www.vox.com/2018/7/10/17553864/reddit-thanosdidnothingwrong-mass-ban-dusting">"Thanos Did Nothing Wrong"</a> (Who else saw this one coming from Vox??? "Explaining the news" since 2014)</p>

          <p style = "padding-left: 2em">- Erik Killmonger in <i>BlackPpPanther</i></p>

          <p style = "padding-left: 4em">First of all...Erik? We've never seen <i>BlackPpPanther</i>, so we've only heard tell of Killmonger. We always just assumed that this was a mononym, like P!nk or Prince, but it turns out that it's actually a...family name? Perhaps an Irish one? And his first name is...Erik? (This feels a little like finding out that Thanos' first name is, like, Ronald, or something.)</p>

          <p style = "padding-left: 4em">At any rate, EriKkKillmonger isn't so much an ecoterrorist, as far as we can tell, but his desire to exchange one race-based hegemony for another definitely feels antihumanist. And critics definitely identified with it. "I fist-bumped in theater!" <a href="https://www.theatlantic.com/entertainment/archive/2018/02/black-panther-erik-killmonger/553805/">one critic wrote</a>. "IT'S A GOOD IDEA." (Caps Lock, of course, original.) "Killmonger is a role model for black children," another <a href="https://medium.com/@jjjones/erik-killmonger-is-a-role-model-for-black-children-d5da7d46b485">suggested</a>. It's safe to say that Erik O'Kilmonger is a relatable villain.</p>

          <p style = "padding-left: 2em">- Richmond Valentine in <i>Kingsman: The Secret Service</i></p>

          <p style = "padding-left: 4em">Perhaps because it takes itself the least seriously, this is by far the best of the movies to appear on this list. Its villain? Richmond Valentine, a Tom Steyer type billionaire who wants to kill off a large portion of the world's population in order to stop the damage he believes they are doing to--you guessed it--the environment. Valentine was included on SyFy's list of <a href="https://www.syfy.com/syfywire/these-environmentalist-sci-fi-villains-deserved-better">"Environmentalist Sci Fi Villains [That] Deserved Better."</a> Guess who else made that list? That's right...Cornelius Thanos.</p>

          <p>And these are, frankly, just off of the dome. Aquaman and Venom also both feature explicit environmental nutjobs. Christopher Nolan has given us ecoterrorists as well, both explicitly in Batman Begins (R'as al Ghul) and inaudibly in Tenet (the...future? Whoever is fighting the retrograde half of temporal WWIII? Honestly, the foundations of the conflict in this movie are as incomprehensible as the eco-philosophies of the villains listed in this intro.) And, universally, critics love them. (See, for instance, <a href="https://earthbound.report/2020/09/11/tenet-climate-change-and-intergenerational-justice/">"Tenet, Climate Change and Intergenerational Justice,"</a> which, surprisingly, is not a manifesto about sound mixing.)</p>

          <p>You can learn a lot about a time by its movies, and our movies seem to be telling us that people aren't necessarily all that opposed to destroying planets/populations/universes/sound mixes in the name of saving the environment. These are our relatable villains. (Which, in a world in which an environmentalist with a movie villain name like Al Gore can run for president, doesn't actually feel all that far off.)  With characters like these in all of our theaters, it's no wonder Greta Thunberg plays hooky so much!</p>

