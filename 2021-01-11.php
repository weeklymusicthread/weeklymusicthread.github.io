          <blockquote class="blockquote">"Intensive care unit" - Avatar (2009)</blockquote>

          <p>We at the WMT have been thinking a lot lately about the way that society seems to be fracturing vis-a-vis the entertainment media landscape. Whereas before, most people got their television programming through three or so major networks, movies from big studios, books from big publishers, and music from big labels, now all of these things are increasingly democratized. All of us are more and more empowered to find ever smaller niches that fit our tastes, and are granted direct access to the artists who cater to those tastes. The age of blockbusters, platinum records, and New York Times best sellers probably isn't over, but these phenomena feel increasingly like the exceptions rather than the rules.</p>

          <p>So too does separation between stars and their fans. Celebrities of the past nonetheless maintained some separation between themselves and the public, but social media has eroded that safe space. With accelerating access to the inner lives and thoughts of our celebrities--and lower and lower thresholds for their "cancellation"--are we moving into an era in which celebrity status itself is becoming both obsolete from a distribution perspective and almost "objectively" undesirable from a personal one?</p>
          
          <p>Take, for instance, Grimes. Increasingly, Grimes is separating her offline life from her digital life, which she is partitioning into her online avatar, WarNymph. Aside from allowing her to, for example, continue promotional photoshoots even throughout the late stages of her recent pregnancy, the avatar allows Grimes to sidestep the difficulties which come with being a celebrity in the age of social media. "It's hard on the psyche to have so much negativity thrown your way on a daily basis via social media. I hear about how ugly or stupid or annoying I am every day." We're sure hubby Elon Musk can sympathize. (No doubt this has been a subject of pillow talk between the two?)</p>

          <p>Celebrity avatars also allow for engagement in ways that weren't possible before. Even in the midst of a pandemic, with large gatherings prohibited nearly worldwide, Travis Scott and other musicians were able to perform digital proxy concerts within Fortnite. We don't wonder so much whether these things will grow in popularity--it seems certain that they will--but rather how far it will go.</p>
          
          <p>WarNymph, for example, resembles Grimes, and is obviously a product of hers. Travis Scott performed "as himself" in Fortnite. These digital avatars are simply stand-ins for a real person who we know exists, and whose identity is known to us. But when will we witness the rise of (initially de facto) synthetic celebrities? When will meatspace humans start choosing to remain anonymous, and gain popular followings merely through their digital avatars? And when AI develops enough, when will organically synthetic celebrities arise out of its algorithms? Will we eventually swap <i>A Star is Born</i> for <i>An Avatar is Born</i>?</p>

          <p>And once that's true in the art world, why wouldn't it be true in gaming, commentary, even politics? Politicians and journalists probably receive more vitriol than any other sector--who wouldn't outsource that to an emotionless AI, given the opportunity? Once celebrity is automated, and when personal details are plastered across every social media platform imaginable, moreover, will we see a flight to anonymity? Will all of those millions of books, movies, and songs about moving to New York or LA to try and make it big give way to works of art about the opposite? Are we in for a total reversal of all of these things?</p>

