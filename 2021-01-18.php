          <p>And now, for the latest installment in <i>The Adventures of Johnny Drumsticks!</i></p>

          <p>Some monikers are unearned. (Chinese Checkers are famously, for instance, neither Chinese, nor checkers.) But not so with Johnny Drumsticks! As a young man, JD lived up to his name, keeping both time and promise as he percussed his way through bands and across stages.</p>

          <p>As with many amateur artists, Johnny D. and his bands were eager to get their name(s) out there, and would play for anyone who would listen. This, of course, included many venues in which people wouldn't listen, such as empty shopping mall parking lots, Chuck E. Cheese's, and even the occasional adolescent bday party. It also meant that you said yes to any gig and venue, without thoroughly (or often even at all) vetting them first.</p>

          <p>So it was for a show one band picked up several hours away. The organizers asked J. Drumsticks and his associates to drive out for a show, and promised them gas money. (This was as good a living as any of them were ever going to make playing music, though they didn't know it at the time.) The boys excitedly accepted it. I mean, gas money? That's a government salary.</p>

          <p>When they got there, however, things turned sour. The booking was a bad one; the organizers had meant to book a different garage band by the same name. Who, you might ask, would know the difference? Well, probably everybody, as this skinny jean indie rock outfit had just showed up to a death metal concert.</p>

          <p>Nevertheless, they persisted. The boys bravely took the stage, and played to nobody in particular. Nobody, that is, until the metalheads started to notice that what they were hearing wasn't just uninteresting to them--it was actually actively offensive. Thus began the boos. They were isolated at first, individual. But soon they grew into a chorus which was impossible to ignore. As the boys finished one track, they huddled up for another.</p>

          <p>"Is it even worth going on? These guys hate us."</p>

          <p>"We can't just stop in the middle of a set."</p>

          <p>"What if we just played the Killers cover and got out of here?"</p>

          <p>Now here was a great idea. The Killers cover--"All of These Things That I've Done"--was a showstopper. The boys had been playing it for a year or two, and being as recognizable as the song was, it always got a good reaction out of the crowd. The lead turned to the mic: "Who here likes the Killers???"</p>

          <p>At this point, one particularly loathsome metalhead in the second or third row of people stepped forward. For some reason, everyone else got quiet.</p>

          <p>"The Killers fucking suck," he said coldly. "And so do you."</p>

          <p>"Thank you," said the lead into the microphone, "and goodnight!" And with that, the boys hustled their gear, to the sound of further booing, off of the stage and into the van, escaping narrowly with their lives, but not their pride.</p>

