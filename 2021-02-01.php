          <blockquote class="blockquote">“Good mixing is re-mixing” – grade school proverb</blockquote>

          <p>This week, we spent some time revisiting (read: playing on repeat) a star from a recent edition of the WMT: QT’s “Hey QT.” That track was released in 2015 essentially as a single, on an EP of the same title, with just one other track: a Diplo remix of the same song. We had never before listened to the Diplo remix, but because we had downloaded not just the single but the “full” “EP,” the first track finished and ran into the second.</p>

          <p>The Diplo remix was unsurprisingly unimpressive. That’s no commentary on Diplo or his/her remixes—Diplo to us is like DJ Khaled in that we’ve heard the name but never any of the music—but a commentary on remixes in general. And that’s because we’ve never heard a remix we liked (at least, not more than or even as much as the original).</p>

          <p>Why is that? It can’t possibly be that all artists who participate in remixing are somehow deficient. In statistical terms alone, that would be anomalous, and that’s saying nothing of the fact that many remix artists write their own perfectly successful original tracks. We suspect, rather, that it’s something about the <i>form</i>. That the remix itself is often doomed, from the outset, to fail.</p>

          <p>With a little bit of reflection, it’s not hard to see why. To explain, let’s start from the beginning. An original song is written, recorded, produced, released. If it’s unremarkable, things mostly end there. But if it happens to catch on, the story continues. The song gets played, it gets shared, and, especially in the Internet age, its reach grows. As that reach grows, the song shows up on the radars of more and more people, some of them musicians themselves. Some of these musicians decide to try to catch the wave and remix the track. So, as you see, in many cases, the decision to remix is inherently linked to the popularity of the original itself.</p>

          <p>At this point in time, however, the original is established. People are listening to it, loving it, and forming a very specific idea of it in their mind. They even hear it when it isn’t actually playing. They hear it in their head, or whistle it while they walk around, or hum it to themselves when they’re alone. They develop a very specific set of expectations for that track. And when the remix comes on—when they hear the opening chords or recognizable lyrics that signal to their brains that the song is playing—those specific expectations are evoked. And when they realize that what they are hearing is not the original track they love but actually a remix, the feeling they get is often one of unmet expectations. Disappointment. A letdown. The remix, which is new and different, gets compared in their minds with the original, which is a sure thing, and so usually, the remix loses.</p>

          <p>There’s also got to be an SEO element to this. Artists seeking exposure must realize on some level that by remixing popular tracks, they attach themselves to those tracks. Anyone who searches for the original has a good chance of coming across the remix on Google, Bing, Yahoo!, Ask Jeeves, Duck Duck Go, YouTube, Rumble, Spotify, Tidal, Facebook, Myspace, Instagram, Snapchat, Tik Tok, Vine, Twitter, Parler, Tinder, Bumble, Coffee meets Bagel, Christian Mingle, Match.com, eBay, Uber, Lyft, and AOL Instant Messenger, and the artist thereby stands to gain recognition. This, of course, forces us to ask ourselves whether remixes are just digital business cards for laptop-wielding musicians.</p>

          <p>But back to the original complication with remixes. Their almost inherent inability to live up to the original isn’t just confined to these sorts of pseudo-singles; we see the same exact problem with the sophomore efforts from most artists. It’s no secret that it’s notoriously difficult to follow up a successful debut album, and most artists who find success early fail to do so. Why is that? No one knows for sure, obviously (if they did, people wouldn’t have this problem), but we suspect that it has a lot to do with the dual pressures of creating something new and reproducing the same sounds that resonated with fans on the original effort. In other words, something about the first record was so special that people fell in love with it. That creates pressures (artistic, financial, and otherwise) to capitalize with a follow-up. (A similar logic applies to covers of successful songs, although the stakes obviously feel much lower there.)</p>

          <p>When that follow-up appears, people will be looking for more of what they loved, but with a catch. Two catches, actually. The first catch is that it can’t be too similar. That would be at best lazy and boring, and at worst cynical and exploitative. But the second catch is diametrically opposed, because it also can’t be too <i>different</i>. If it’s too different the fan base from the initial effort is lost, and the artist has to start over and generate an entirely new following.<p>

          <p>By the way, we suspect this is also why unplanned sequels tend to be so poorly received. It’s one thing if a story is <i>planned</i> as an initial installment in a series because, presumably, there’s already a plan in place for how to build on it. But when a sequel is generated purely due to the success of the original, these twin pressures come to bear. Almost no one seems to be able to navigate them effectively, which is why the critically AND commercially adored unplanned sequel is a rare beast indeed.</p>

          <p>At any rate, we’re not saying that remixes, sophomore efforts, covers, or unplanned sequels are defective or somehow inherently without value, just that they are inherently more complicated to pull off successfully. They tend to be uphill battles. (There does seem, of course, to be one exception to this, which is R. Kelly’s “Ignition (Remix).” But that seems to be an exception which proves the rule, as the original “Ignition” was not particularly successful and did not garner much of a following.)</p>

