          <blockquote class="blockquote">“How often have I lain beneath rain from my strange roof, thinking of home?” – Emily</blockquote>

          <p>We write to you, as we’ve implied to this point, estranged and expatriated. And it <i>is</i> strange and…patriate? Whatever. At any rate, things—things like the customs, things like the language—are different. Even the simplest tasks become almost impossible in another country. (This is true to such a degree that you can apparently sell Netflix on at least one season of it. Then again, is that saying much?) Like, for instance, dealing with an increasingly aggressive leak in the ceiling of the apartment you moved into only two weeks ago.</p>

          <p>We need not explain that this creates problems: problems with neighbors, problems with landlords, and problems with furniture. Problems with insurance companies. Not least of all, though, it creates problems with <i>sleeping</i>. And for several reasons. We are stressed, for one, by the situation itself, and by the anticipation of whatever it is that comes next. We are anxious that we will wake up damp or soaked or even afloat on the upholstered raft of our sofa bed. And then, most of all, we are persistently annoyed by the <i>drip, drip, drip</i> of this particular, spontaneous form of Chinese water torture.</p>

          <p>And so it is that we plug our ears against the noise, first with fingers and then with pillows and then, finally, with ear buds. And slowly, as we struggle to fall asleep, we proceed retrograde through the back catalogue of an audiophile life, until we’re rediscovering records we haven’t heard in decades, and with them, the memories we were busy forming when we first discovered them. (If Proust had his <i>madeleines</i> then we have, we suppose, some songs on Spotify?) We’re by turns affected and embarrassed to remember both. Bus rides. Records that don’t hold up. A girl who kissed us once and who we later stood up.</p>

          <p>We won’t share them here, and anyway, they wouldn’t land. So we’ll end just by saying that, once in a while, you should delve into your own back catalogue. You might be surprised by what it brings back with it.</p>

