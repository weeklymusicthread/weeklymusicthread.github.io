          <p>This week, from the vault, we bring you…a change of pace. Maybe it’s just homesickness on the part of this expatriated American, but in those few moments when we’ve broken from all of the other great music mentioned above and below on this week’s installment of the WMT to come up for air, we’ve been revisiting George Strait’s “Check Yes or No.” This is the country music equivalent of a Glock: it’s just well built. Period.</p>

