          <blockquote class="blockquote">“All good things must come to an end” – Guy-Manuel de Homem-Cristo and Thomas Bangalter</blockquote>
         
          <p>One of the drawbacks to running a weekly music thread is that, six times out of seven, we’re behind the times. And so it is this week, with the demise of Daft Punk. Surely, by now, anyone who would read a blog like this is already well aware of the break-up. But nevertheless, we can’t help reminiscing.</p>

          <p>We remember, for instance, listening to “Digital Love” on repeat for days on end with roommates in our college dorms, as both a prelude to pregames and a soundtrack to silly college crushes. (By the way, does anyone else hear the line “There’s nothing wrong/with just a little, little fun” as “There’s nothing wrong/with just a little elephant”?) Or the way that <i>Random Access Memories</i> took over parties and hangouts for a solid six months when it came out in 2013. Or, maybe most of all, our belated discovery just last year of the surprisingly fun <i>Tron: Legacy</i>, in which Daft Punk make a cameo.</p>

          <p>On that last note, the complete edition of the original soundtrack for <i>Tron: Legacy</i> is really excellent. It’s up there for us with the soundtracks for <i>The Lord of the Rings</i> and Jim Jarmusch’s <i>Dead Man</i> (in which Johnny Depp and Gary Farmer read the poetry of William Blake over Neil Young’s electric guitar) as some of the best writing music there is. (By the way, if you haven’t seen <i>Dead Man</i> a hundred times by now, or listened to the original soundtrack a thousand, it’s kind of like, what are you waiting for, you know?)</p>

