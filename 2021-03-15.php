          <h2 class="section-heading" style="font-size:125%">From the Vault</h2>

          <p>This week, from the vault, we bring you Etta James’ rendition of “At Last.” Although readers of our age may remember this song mostly from a combination of <a href="https://www.youtube.com/watch?app=desktop&v=3o4KqbRqmgg">every commercial ever made</a> and the 2004 and 2005 World Series celebrations for the Boston Red Sox and the Chicago White Sox, respectively, we were reminded of it today by two crooning bus boys while ordering a sandwich at a local Portuguese deli. Revisit Etta’s version, and don’t be afraid to pair this with Brian Fallon’s “Etta James,” a recent ode to the song and the singer herself. What you <i>should</i> feel comfortable skipping, however, is Beyonce Knowles’ soulless, sanitized, and, unfortunately, ubiquitous cover. It feels rehearsed, obligatory, and without emotion, sort of like the inaugural Obama dance she sang it for. Etta, for her part, agrees:</p>

