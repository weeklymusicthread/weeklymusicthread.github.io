          <p>As this week’s WMT approached, we found ourselves at an impasse: we had something to write, but nothing to write it about. That’s right, folks—writing a Weekly Music Thread isn’t all fun and games. It’s hard, important work, and it makes us David Bowie. (Which is to say, it makes us heroes. Heroes in the fight against COVID, and heroes in general.)</p>
          
          <p>At any rate, we were out of ideas. So we scoured the internet, and found a Pitchfork article on which Grammy nominees <i>should</i> win each award, and which Grammy nominees <i>will</i> win each award. After spending several hours in intellectual paralysis over the unanswerable question of which is less relevant in 2021—Pitchfork or the Grammys—we eventually realized that there was an even bigger question to be asked here: “wait, the Grammys are tonight?” (That’s right, guy. We write these on Sundays.)</p>

          <p>And yes, the Grammys are tonight. (Again, Sunday, guy.) And as we read Pitchfork doing its best impression of the precog gypsy soothsayer Nate Silver, we couldn’t help but ask ourselves: what is going on here? Phoebe Bridgers is nominated for Best New Artist something like half a decade into her career. The same artists are nominated for the same works in both Rock and Alternative Music categories. There are categories for R&B, Traditional R&B, and Progressive R&B. And to top it all off, the number of nominations in each category has swollen to GME-like proportions.</p>

          <p>How…could this have happened? Well, we’ll tell you how: because the Grammys, like the Oscars, and any other industry awards show, is a process by which members of an industry vote awards to other members of the same exact industry based on almost anything but how good their works actually were. Did William “Billie” Eilish really make the best record of 2020? Was <i>Argo</i> really a better movie than <i>Django Unchained</i>? Was <i>Spotlight</i> a better movie than <i>Call Me By Your Name</i> (which might be the single most artful thing that’s been made in the past decade)? Looking back over lists of previous winners of these awards is an exercise in incredulity. And a certain point, you have to ask yourself: Are the Grammys really just Employee of the Year awards, but for music?</p>

          <p>Maybe. But, then again, maybe not. After all, “Employee of the Year” implies employment, and most of the musicians we know are unemployed. Moreover, the Grammys aren’t really conferred by a CEO or an industry: they’re conferred by <i>“the Academy.”</i> It’s a school of people—teachers and students of the craft, we suppose—who vote for each other for special prizes while everyone else watches and rolls their eyes. Which makes the Grammys a cross between…prom court and student council elections, only somehow more self-important. The only thing both sillier and less consequential are, as far as we can tell, real elections with actual politicians. </p>

