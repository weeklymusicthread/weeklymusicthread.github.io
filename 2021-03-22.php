          <p style="text-align:center"><b>“Bed Rock” by Young Money—A Close Reading/Explication</b></p>
          
          <p><i>Uh, she got that good-good, she Michael Jackson bad</i></p>
          <p style="padding-left: 3em">While scholars dispute the meaning of “good-good,” the reference to Michael Jackson in the back half of this line refers to Jackson’s hit album, <i>Bad</i>, from 1987. The word “bad,” however, may also be used to describe an attractive female. Thus, the lust object at the center of “BedRock” is described as “Michael Jackson bad.”</p>
          
          <p><i>I’m attracted to her, or her attractive ass <br>And now we murderers, because we kill time</i></p>
          <p style="padding-left: 3em">“Killing time” is a popular American idiom used to describe the act of being idle. Because the act of killing another human being is known as “murder” (or, alternatively, “homicide”), the artist here humorously likens himself in his languor to a murderer.</p>
          
          <p><i>I knock her lights out but she still shine<br>I hate to see her go, but I love to watch her leave</i></p>
          <p style="padding-left: 3em">Here, Wayne continues his wordplay. To “knock one’s lights out” originally meant to land such a devastating blow on an opponent during a boxing match that one’s opponent was rendered unconscious; since entering the popular lexicon, however, the term has come to be used colloquially to simply mean “impress.” As used by Wayne, this means that he impresses his female companion, but that, even in her adulation of him, she continues to impress him as well.</p>
          <p style="padding-left: 3em">In the next line, Wayne establishes a pattern of wordplay which will resurface throughout the rest of the work. When he says that he “hate[s] to see her go,” Wayne implies regret at the departure of his romantic companion. By clarifying, however, that he “love[s] to watch her leave,” the artist insinuates the physical allure of his partner. Although parting is such sweet sorrow, in other words, the blow is softened by the opportunity it affords to ogle this woman. </p>
          
          <p><i>But I keep her running back and forth; soccer team</i></p>
          <p style="padding-left: 3em">The object of the narrator’s affections visits him continually. In the sport of soccer (called “football” in countries other than that of the song’s origin), various players compete to place a ball beyond a goalkeeper and into the back of a net. Because a traditional soccer match takes place between two teams, and because these teams each defend one side of the field, players must run back and forth between them for the duration of the game. For that reason, the narrator likens his paramour to a soccer team.</p>
          
          <p><i>Cold as a winter’s day, hot as a summer’s eve<br>Young Money thieves steal your love and leave<br>I like the way you walking if you walking my way<br>I’m that Red Bull, now let’s fly away</i></p>
          <p style="padding-left: 3em">Red Bull is an Austrian energy drink which was marketed in the United States with the catch phrase “it gives you wings,” presumably due to the physiological effects of beverage consumption (including negative cardiac and mental consequences). Here, the artist likens himself to Red Bull, and then, playing off of the implied slogan, suggests that he and the object of his affection “fly away” together.</p>
          
          <p><i>Now let’s buy a place, with all kind of space<br>I’ll let you be the judge, and-and-and I’m the case</i></p>
          <p style="padding-left: 3em">Because she judges him? Because she sits in on him? Because she presides over him? This line challenges conventional notions of the relationship between judges and their cases.</p>
          
          <p><i>I’m Gudda Gudda, I put her under</i></p>
          <p style="padding-left: 3em">[unintelligible]</p>
          
          <p><i>I see me with her, no Stevie Wonder</i></p>
          <p style="padding-left: 3em">Stevie Wonder is an American musician who worked primarily during the twentieth century, and whose work endures in popularity to this day. Shortly after Wonder was born prematurely, however, he suffered from retinopathy of prematurity, rendering him blind. Here, Gudda Gudda clarifies that, because he can envision himself with the woman he’s discussing, he is no Stevie Wonder, i.e. he is not blind.</p>
          
          <p><i>She don’t even wonder, ‘cause she know she bad<br>And I got her…grocery bag</i></p>
          <p style="padding-left: 3em">From Wikipedia: Grocery bags are medium-sized bags, typically around 10–20 litres (2.5–5 gallons) in volume (though much larger versions exist, especially for non-grocery shopping), that are used by shoppers to carry home their purchases. Some are intended as single-use disposable products, though people may reuse them for storage or as bin liners, etc.; others are designed as reusable shopping bags.</p>
          <p style="padding-left: 3em">Thus, because Gudda Gudda “got her,” well, grocery bag.</p>

          <p><i>Ooh, baby, I be stuck to you like glue, baby</i></p>
          <p style="padding-left: 3em">Glue is a commercially available adhesive which is sold in liquid, solid, and even “rubber cement” forms. In all cases, glue is used to bind two substrates to another with varying degrees of strength. To be “stuck to [someone] like glue,” therefore, implies the sort of depth of emotional connection which is described throughout the piece.</p>

          <p><i>Wanna spend it all on you, baby<br>My room is the G-spot</i></p>
          <p style="padding-left: 3em">By dubbing his room the “G spot,” a reference to the female erogenous zone theorized by German gynecologist Ernst Grafenberg, Lloyd here insinuates prowess as a lover. The G spot name, however, works on two levels: in addition to its gynecologic connotations, the fact that it is Lloyd’s own room in question could also be taken to mean that Lloyd himself is a “G,” a street term which is commonly used as an abbreviation for “gangster”.</p>
          
          <p><i>Call me Mr. Flintstone, I can make your bed rock</i></p>
          <p style="padding-left: 3em">The Flintstones was a popular children’s television cartoon which ran on ABC between 1960 and 1966. The series followed the daily exploits of the fictional Flintstones, a pre-historic family living in the Stone Age town of “Bedrock.” Hence, the play on words: because Lloyd’s sexual maneuvers rock the bed, and because Fred Flintstone lives in Bedrock, he asks the listener to refer to him as “Mr. Flintstone.”</p>
          
          <p><i>Okay, I get it, let me think, I guess it’s my turn<br>I guess it’s time to put this pussy on your sideburns</i></p>
          <p style="padding-left: 3em">Presumably a reference to oral sex, but the logistics seem…unorthodox. </p>
          
          <p><i>He say I’m bad, he probably right<br>He pressin’ me like button-downs on a Friday night</i></p>
          <p style="padding-left: 3em">Again, presumably a reference to the button-down shirts worn by young men on evenings out on the town, but we imagine that these outfits are usually pressed prior to Friday night, no? Like, during the week? </p>
          
          <p><i>I’m so pretty like, be on my pedal bike<br>Be on my low starch, be on my egg whites<br>He say, “Nicki, don’t step, you da bestest”<br>And I just keep coming off the top, asbestos</i></p>
          <p style="padding-left: 3em">In this verse, Nicki Minaj asserts that she “just keep coming off the top,” whatever that means. She then likens herself to asbestos, a friable silicate material which was popular for its qualities as insulation throughout the twentieth century. Because ceilings are insulated, and because Minaj keeps coming “off the top,” and because ceilings are situated at the top of rooms, Minaj likens herself to asbestos.</p>
          
