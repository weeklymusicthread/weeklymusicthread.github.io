          <blockquote class="blockquote">“You sound like you’re from London” </blockquote>

          <p>Something funny we’ve noticed:</p>

          <p>You speak with someone who’s learning your language. “I’m sorry for my accent,” they tell you. They seem embarrassed, genuinely apologetic. “But your accent isn’t bad,” you reply. “It’s actually quite nice.” And you mean that, too: English never sounds quite so nice as when it’s spoken by a non-native speaker. (We have discovered, for instance, that French speakers who learned English from Brits speak their second language with a hybrid French-British accent, which is more fun than you can imagine.)</p>

          <p>And then there’s the reverse. You start to speak another language. You learn the vocabulary, practice your pronunciation until it’s perfect, and finally, when you get the chance, you speak it out loud to a native speaker. You’re nervous at first—of course you are—but as you start to get the words out, you realize you’re not actually doing all that bad. If anything, you sound pretty good. You finish your sentence and stand back, as you’re impressed with yourself. And for a moment, it seems as though everyone else is, too. They’ve all stopped, in fact, in stunned silence. And then, all of a sudden, you realize that it isn’t that they’re impressed. They stare at you, stopping long enough only to stare at one another, before they turn back once again to stare at you.</p>

          <p>“Quoi?” One of them asks.</p>

          <p>You repeat yourself, but to no avail. You try a second time, a third. Nothing. And then one of them realizes what you’re trying to say, and, with a great extended “Ohhhhhhh,” they repeat it back to you. It sounds <i>exactly</i> the same to you as what you just said, but empirically, it obviously cannot be. The evidence is in, and you can’t tell the difference. </p>

          <p>Having this experience multiple times every day has taught us something: you can’t hear your own accent. (You may recognize a variant of this, which is that no one thinks of themselves as speaking with an accent. An accent is only ever something that <i>other</i> people have.) You can tell when you’re <i>mispronouncing</i> things, but you can’t really detect your own accent, which is something else entirely.</p>

          <p>It reminds us of a middle school assignment we once had. We were paired with classmates and asked to record a “news report” on a current event. To our luck (or lack thereof), we were paired with the prettiest girl in the class—a situation which finally forced us into conversation with her. We worked on the report together and, when it was ready, we made the recording. Afterwards we played it back to make sure it sounded okay. As we heard our voice coming through the tape recorder, we cringed. It sounded awful! Somehow throaty and nasally at the same time, as though Louis Armstrong had caught cold and then pinched his nostrils shut in a cannonball dive to boot. The girl shifted uncomfortably in her seat. <i>Quick</i>, we thought to ourselves. <i>You have to do something, anything. You have to save yourself from this situation, and win her heart. Girls like self-deprecation—try that!</i></p>

          <p>“Yikes,” we forced out with an awkward laugh. “Is that how my voice sounds on recordings?”</p>

          <p>“What do you mean ‘on recordings?’” she asked, genuinely confused. “I think that’s just how your voice sounds in general.”</p>

