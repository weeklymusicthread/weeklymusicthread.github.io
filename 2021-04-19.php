          <h2 class="section-heading" style="font-size:125%">From the Vault</h2>

          <p>This week, from the vault, we bring you The Gaslight Anthem’s <i>Get Hurt</i> (2014). The first record lead man Brian Fallon made after his divorce and the last one the band made before their break-up, <i>Get Hurt</i> marked a change of direction for the band that confused a lot of critics at the time. That being said, there’s a lot to like on this record. Honestly, the entire front side of it is a pretty rewarding listen, and latter side tracks like “Ain’t That a Shame” and “Dark Places” will stick with you as well. This marks one of the rare times, moreover, that a deluxe edition actually delivers. “Mama’s Boys” and “Halloween” both are worth your while, as is “Have Mercy” from the iTunes Store deluxe edition. In all, Get Hurt isn’t <i>Sink or Swim</i>—Gaslight’s first and greatest record—but it’s a worthy entry in their catalog and would have received effusive praise coming from any other group.</p>

