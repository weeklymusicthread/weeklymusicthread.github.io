          <blockquote class="blockquote">And now, for the latest in…The Adventures of Johnny Science!</blockquote>

          <p>His father was a foreigner, a Frenchman, who had trained in the ways of the heart here, in Paris, the City of Love, before expatriating to the States, where he remained a cardiologist but became an American, watching NASCAR on weekends and starting a family, first and foremost with the birth of a boy, a boy who he called Johnny, and who, when he was presented to the world, the world called Science. Johnny. Science. Johnny Science.</p>

          <p>He raised the boy in his image. He drove him to school in a mobile cath lab, until, bored of it, the boy left school altogether and cut his teeth in the clinic, taking blood pressures for pennies and listening to heart murmurs the way other kids listened to Kidz Bop. By the tender age of seven, the boy had showed such potential that the father had got the idea in his head to start to train him seriously. And so, one night, he took the boy by the hand, grabbed some pots and pans, and sat him outside in the midst of a late summer downpour. He tossed a pile of electrocardiograms into the boy’s lap. “Read them,” he said, and began to smash the pots and pans together. The boy looked at his father, looked up at the sky, looked down at the pile of EKGs. And he started reading.</p>

          <p>They did this every time it rained. And in those moments, outside, under the pouring heavens, when the father taught his son everything he knew, he began with the beginning. “Cherchez le ‘p,’” he muttered in his native tongue, almost under his breath, like a prayer. “Cherchez le ‘p.’”</p>

          <p>The phrase, like the father, came from France. “Cherchez la femme, pardieu!” wrote Dumas. “Cherchez la femme!” And, elsewhere: “Il y a une femme dans toutes les affaires; aussitot qu’on me fait un rapport, je dis ‘cherchez la femme!’” From there, it found its way into film noir, a tradition which finds a female, a femme fatale, at the dark heart of every mystery. And it was the father who, in the spaces in between shifts at the hospital and late night detective stories, found the phrase, reads its rhythm, resurrected it, rehabilitated it for his own purposes, for the dark hearts at the center of his own living, breathing, congestive mysteries. He was Nick Charles in a white coat, Philip Marlowe with a stethoscope. Because he followed leads too, you see: aVR, aVL, and aVF, to be exact, with V1-V6 thrown in to boot, wiretaps to help him find the pulse, pathways etched on paper to his telltale hearts.</p>

          <p>But no longer was it “cherchez la femme.” No, here, it became “cherchez le ‘p,’” the p-wave, the inciting event, atrial depolarization—a burst of energy that kicks off the story, starting the squeeze that eventually leads to blood. And it was the p-wave, the father found, that held all the secrets. “Cherchez le ‘p,’” he told himself for years, and the secrets to EKGs yielded to him as though they were pulp fiction and he were Humphrey Bogart. He said it to himself. He said it to his students. And he said it to his son, Johnny Science, the Prince of Hearts, outside, in the pouring rain, night after night, as he taught the boy to read EKGs. </p>

          <p>And read EKGs the boy did. By the end of the year, he was reading at a medical school level. By the time he was in medical school, he wasn’t even reading the EKGs anymore—they were reading him. But medicine wasn’t everything, and Johnny was a renaissance man. He had a variety of interests, the arts among them, and in the early 2020s, he began to record and transmit his thoughts on emerging musical artists. It was thus that, in the spring of 2021, he re-wrote his father’s phrase. It was still “cherchez,” of course—it was always “cherchez”—but no longer was it the ‘p.’ No, everything he had learned over the year which preceded was that, if you wanted to find good music, you had to “cherchez” something else. You had to “cherchez le Strange,” as in, Bartees. If you find Bartees Strange, you’ll find the music you’re looking for. After “Mustang” and “Goldbrick Champion,” the additions on this week’s WMT prove it.</p>

