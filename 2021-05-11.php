          <blockquote class="blockquote">'It's then I smell smoke. "You even smoke while you brush your teeth?" She looks at me sideways. "Menthol," she says.' - Janet Woodcock, Meddlesex</blockquote>

          <p>Well, loyal readers, as you may have noticed, we've been away for a bit--but just a bit. You can rest assured that we didn't leave; we only stepped outside. You can call it a smoke break. </p>
          
          <p>While we were out, the U.S. Food and Drug Administration (FDA) announced that it intends to ban menthol cigarettes within the next year. Now, we know what your next question is going to be: what in the heck is the U.S. Food and Drug Administration (FDA)? (You could, of course, be forgiven for thinking that here is an organization which administers food and drug; the truth is, kind of, the opposite.) It's too complicated to go into here, but basically, they tell you what sort of things you can and can't light on fire and then breathe in. </p>
          
          <p>For instance, if you dried out some tobacco, you could light that on fire and then breathe it in. But if you dried out some tobacco and then soaked it in mint, you could NOT light that on fire and then breathe it in. </p>
          
          <p>Now, as anyone who has ever heard music will know, things that you can light on fire and then breathe it in don't only have their place between our lips and fingers--they've also got their place in our songs and lyrics. Even their paraphernalia have a presence. Doesn't the U.S. Food and Drug Administration (FDA) remember when we used to go to concerts and wave our lighters in the air? Or do they just not care?</p>
          
          <p>But seriously--let's forget cigarettes in general, and focus on those menthols for a moment. A simple Google search of "menthols song lyrics" returns hundreds of songs built on cornerstones of packets of Newports. And none more meaningful to us than one that takes us back to London in 1978, with the sophomore effort of a little band you may have heard of called The Clash. Give 'Em Enough Rope. Side two. Track three. "Stay Free." "Got me in bad fights/play me pool all night," sings Joe Strummer. "Smokin' menthols." ("Stay Free." What a prescient, libertarian protest anthem.)</p>
          
          <p>The U.S. Food and Drug Administration (FDA) would deprive us of menthols. This, we know. Would they also deprive us of "Stay Free?" Of The Clash? To that, we're sure, they'd say, "wait, who?"</p>
          
          <p>No, not The Who, The Clash. No doubt the folks at the U.S. Food and Drug Administration have never heard of either. And yet, they're ready to write out of existence one of the best tracks from the latter. </p>
          
          <p>But, on the other hand, when did a government ban ever make anything less cool? Prohibition gave us Gatsby and gangsters, and it's not as though hip hop lyrics have taken a hit as a result of the prohibition of marijuana. (Thalidomide, admittedly, has lost a little of its cache and social currency.)</p>
          
          <p>So maybe, then, we've got it backwards. Maybe the U.S. Food and Drug Administration (FDA) isn't out to stub out menthols, but to light a subcultural fire under them. By banning them, they reinvigorate them. They make them cool. Give them a breath of fresh, minty air and, in so doing, Make Menthols Great Again.</p>

