          <h2 class="section-heading" style="font-size:125%">From the Vault</h2>

          <p>This week, from the vault, we bring “Jenny Was a Friend of Mine” by The Killers. The opening track off of their 2004 debut Hot Fuss, this track is sometimes overshadowed by “Somebody Told Me” and “All These Things That I’ve Done.” From first listen, however, this one—inspired by a real-life murder confession and told from the perspective of a jealous boyfriend being questioned about the circumstances surrounding his girlfriend’s death—instantly grabbed, and then always held, our attention. It’s an excellent opener to an excellent record, even if it’s more raw than everything that comes after it put together. The screaming synths, the churning drums, and that thick, fatty, chewy, bloody, rare-almost-raw bass line. Perhaps the most interesting thing about “Jenny Was a Friend of Mine” is that it poses a question that it never answers. Our narrator tells us that “she couldn’t scream while I held her close/I swore I’d never let her go.” Did he do it? Didn’t he? (In live performances, Flowers places his finger on the scales when instead he sings “she couldn’t scream while I held her throat” or even “she kicked and screamed while I held her throat.”) Listen to this one on repeat while you try to decide.</p>

          <h2 class="section-heading" style="font-size:125%">The Classical Fascicle</h2>

          <p>Well, folks, here it is: the first installment of the classical fascicle. The entire idea, as described above, was actually born in response to this week’s piece: Sergei Rachmaninoff’s Isle of the Dead. (We’ve been listening to the 1984 Vladimir Ashkenazy/Concertgebouworkest recording.) First of all, there is a backstory to this piece that existing fans of classical music probably already know well. Rachmaninoff was inspired to create it upon seeing a black-and-white print of the famous Bocklin painting by the same name. Later, however, upon seeing the image in color, Rachmaninoff remarked that it disappointed him, and that he probably would not have been inspired to write the piece had he seen the color version first. Lucky for us, we suppose, he didn’t.</p>

          <img src="img/bocklin1.jpg" alt="Bocklin 1">

          <img src="img/bocklin2.jpg" alt="Bocklin 2">

          <p>(P.S. Next time someone scoffs at a director’s decision to film in black-and-white, show them these two images and see if they don’t think it makes a tonal difference. Clearly, it did for Rachmaninoff. Actually, you can also show them Season 1, Episode 2 of the utterly brilliant <i>Neo Yokio</i>, which briefly features this very painting with a midnight blue sky during its climactic fight scene.) As a brief aside, our first introduction to Rachmaninoff must have been 16 or 17 years ago when, one summer day, a diehard metal head and a buttoned-up financier with whom we were lunching bonded over his Prelude in C Sharp Minor. We ran into him (Rachmaninoff) again this week, with Isle of the Dead. From its opening moments, it is dark, cinematic, and foreboding. (It’s also a <i>clear</i> influence on the quieter and more somber moments of Clint Mansell’s “Death is the Road to Awe.” In fact, for those who, like us, have had a hard time breaking into orchestral music, the Mansell track is not a bad place to start. It’s best listened to loud, on very nice headphones, with eyes closed, recumbent, in the dark.) It’s said that the opening movement of Isle of the Dead is meant to evoke the feeling of approaching the island, the sound and rhythm of the oars on the water, or even the waves themselves. The entire piece alternates between edgy and severe, and it really held our attention. We felt like it was accessible, and maybe you’ll feel the same. To be honest, we’re not really sure what else to write here. After all, we barely know how to listen to classical music; how are we supposed to write about it? We’re beginning to realize that this may not have been the best idea…</p>

