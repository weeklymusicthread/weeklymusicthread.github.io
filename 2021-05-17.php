          <blockquote class="blockquote">“Don’t really care for classical music; lot of unnecessary notes” – Charlie Kelly, <a href="https://www.youtube.com/watch?app=desktop&v=Q3vN4N8LbLU">almost</a></blockquote>

          <p>“Where now? When now? Who now? Unquestioning. I, say I. Unbelieving. Questions, hypotheses, call them that. Keep going, going on, call that going, call that on.” </p>
          
          <p>So opens <i>The Unnamable</i>, the third installment in Samuel Beckett’s trilogy. It’s one of the great opening lines of all time—perhaps the great opening line, given that it achieves a level of meta-awareness that would never occur to most others even to attempt. Put otherwise, opening lines are always about <i>something</i>, but rarely are they about opening lines themselves. Set a scene, introduce some characters. Imply a personality and you create it within the reader’s mind—unquestioning. All you have to do is say “I,” and keep going from there.</p>
          
          <p>About 20 years after <i>The Unnamable</i>, another author—Samuel R. Delany—gave us something similar in the opening lines of his circular, post-modern science fiction epic, <i>Dhalgren</i> (which we recommend reading while on honeymoon in Japan). “to wound the autumnal city,” the novel begins. “<b>So howled out for the world to give him a name</b>. The in-dark answered with wind. <b>All you know I know.</b>”</p>
          
          <p>The modernists and post-modernists played extensively with this idea—that our language constructs our reality. This, of course, did not come solely or even primarily from fiction writers like Beckett and Delany, but also from post-modern philosophers. (Even Noam Chomsky, no fan of the post-modernists, tells us that “the structure of language determines not only thought, but reality itself.”) This idea is so powerful that to some it has become almost commonplace, taken for granted. It crops up everywhere you look. It’s in Orwell’s <i>Nineteen Eighty-Four</i> (Big Brother controls its subjects by winnowing down their language and thus depriving them of the words they need to construct certain thoughts) and even the Harry Potter books, in which characters avoid using the villain’s name as though doing so will speak him into being. To make something real, we’ve been taught, you need to speak it into being.</p>
          
          <p>Indeed, that’s where this very thread came from. A New Year’s Resolution a year and a half ago to try and discover, more regularly, great new music to share. In order to hold ourselves to it, we started the thread (which actually did start as a thread, a weekly e-mail between a few friends), because it was easier to force ourselves to find new music when we knew we had to write about it once a week. And it worked: not only have we found plenty of wonderful new music (hello, Ethel Cain), but we’ve found a way to share it with other people, too.</p>
          
          <p>Well, now it’s time to speak something else into being. Not something else entirely, but something related, something WMT-adjacent. And that’s a new sub-section, which we’ll be calling the Classical Fascicle. You see, we’ve wanted to become more fluent in classical music for years--decades, even, at this point—but we’ve never gotten it to stick. There have been a few pieces that have held our attention, famous ones like the 1812 Overture and Beethoven’s Ninth, but for the most part, this entire <i>world</i> of art has eluded us. Well, no longer. We’re committing here and now to further expanding the Weekly Music Thread by including one piece of classical music per week.</p>
          
          <p>Now, it’s important to keep in mind that we have no idea what we’re doing and little clue what we’re talking about. This is, of course, <i>generally</i> true, but it goes ten-fold for classical music. Can you really speak something into being if you’re functionally mute? We suppose that we’re going to find out. We anticipate that this will come off as something of a cross between <a href="https://www.youtube.com/watch?app=desktop&v=nQVnRKIpzAU">Zoidberg’s art collection</a> and <a href="https://www.youtube.com/watch?app=desktop&v=r73C0N8FDek">Leonard’s frozen pizza reviews</a>. But if it’s anything like the WMT itself, we’ll look back in 18 months and be both glad we did it and surprised at how far it’s come. </p>

