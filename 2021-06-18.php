          <p>This raises a clear and obvious question: where do we draw the line between being a novice and intermediate at something? Is there a line? Or perhaps, is it more of a spectrum-like gradient, where your increasing experience shades in what was initially a blank slate?</p>

          <p>After finishing a challenging book, we at the WMT always feel as though we were illiterate before reading it, and that anything we read prior to that book was a total waste. Surely, we always say to ourselves, there were references we missed, words whose implications we didn’t quite understand. This internal war of attrition isn’t something that ends, or something that one should want to end. How would you get any better at understanding existentialist philosophy without reading <i>Being and Nothingness</i>? (For the record, we had the above feeling of illiteracy before, during, and after reading that behemoth.) But <i>that</i> is exactly what we’re saying.</p>

          <p>Getting good at anything takes time. Whether it’s dribbling a basketball, reading Sartre, or writing a music blog post, the first time you’ll probably suck, and the next time should be better.</p>


