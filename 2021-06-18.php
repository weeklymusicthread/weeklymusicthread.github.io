          <p>Doing something for the first time is always… weird. Take playing basketball, or even dribbling a basketball, for that matter. You try it and come to a quick realization of, “wow, I suck at this.” Oftentimes these experiences are uncomfortable, and it is only after hours upon hours of time spent with such an endeavor that you can say, “well, at least I suck less now.”</p>

