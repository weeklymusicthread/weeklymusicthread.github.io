          <h2 class="section-heading" style="font-size:125%">From the Vault</h2>

          <p>This week, from the vault, we bring you Twin Shadow’s “Brace (feat. Rainsford).” The lead track off of 2018’s <i>Caer</i>, “Brace” is a modern, synth R&B take on Tom Petty’s “Free Fallin’” that <i>really, really</i> works, not least of all on repeat in front of a flow cytometer for late night and/or early morning hours and hours on end. You’ll be all like “yeah, I WAS in the back room, dreaming out loud before I found you!”</p>

          <h2 class="section-heading" style="font-size:125%">The Classical Fascicle</h2>

          <p>This week, as part of the Classical Fantastical, we bring you the overture to Richard Wagner’s <i>Rienzi</i>. One of Wagner’s early operas, <i>Rienzi</i> tells the story of “a late medieval Italian populist figure who succeeds in outwitting and then defeating the nobles and their followers and in raising the power of the people. Magnanimous at first, he is forced by events to crush the nobles’ rebellion against the peoples’ power, but popular opinion changes and even the Church, which had urged him to assert himself, turns against him. In the end the populace burns the Capitol.” It is, in other words, an entire fiction which bears no resemblance to the current day state of affairs—a nice break from the rough-and-tumble world of contemporary politics. The overture is a brief, accessible dream with a few really beautiful moments. Check it out, and ready yourself for more Wagner here in the weeks to come.</p>

