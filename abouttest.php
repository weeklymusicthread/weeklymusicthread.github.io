        <p style="font-size:125%">After a year of weekly email exchanges about little more than music, the creators of the Weekly Music Thread launch an underwhelming website that at its best reads suspiciously like an estranged family member’s Facebook feed: boring posts about things that they like, combined with a troubling failure to supplement their music reviews with the appropriate social and political commentary. </p>

        <p>Threads, like the conversations they carry, say something about people. (In fact, this has been true since before Al Gore invented the worthwhile but at this point somewhat overhyped internet—although many listeners may not realize it, the word “threads” also use to mean “clothes,” probably due to their usage in the make-up of those visual style signifiers.) But threads are also wavy, wiggly little thingies, like strings, and just as one carefully woven thread can make up a beautiful tapestry or cloth face mask, so too can an errant one unravel your alternative winter cardigan into a tangled and knotted mess.</p>
        
        <p>It’s a lesson the folks over at the Weekly Music Thread would do well to learn. Established at the very outset of 2020, it’s not unreasonable to think that this casual weekly email exchange amongst friends at the very least heralded (but also very probably <i>caused</i>) the worst year in the history of music, pandemics, and, quite frankly, America itself. Still, we would be remiss not to recognize that the original iteration of the WMT held at least some level of rudimentary charm: as an ongoing, irreverent celebration of music its contributors liked, the WMT poked fun at anything and everyone while uncovering some blogosphere-approved tracks and records along the way. It also established certain tropes: constant misattribution of quotes, for example, or ever-evolving portmanteau pseudonyms associated with each post.</p>

        <p>That approach, however, had its shortcomings. Fun and liking music are <i>fine</i>, of course, but at a certain point, internet readers want something <i>more</i> out of their music websites. They don’t just want to be told what writers <i>like</i>. They want to be told what’s good and what’s bad (mostly what’s bad), and what they should think about any given piece of music or any given musician—not just personally, but socially and  politically as well. They want precision to the level of a tenth of a decimal place. They want to be constantly reminded about registering to vote. They want a sense of taste and a set of opinions—about music, sure, but about all things, really—to be cultivated for them so that they don’t have to expend any effort and also probably fail at correctly cultivating their own. And nobody was ever going to get any of that from the WMT.  </p>

        <p>Well, now the creators of the WMT are back, and they’ve gotten the proverbial band back together for an effort at a website: <a href="www.weeklymusicthread.com">www.weeklymusicthread.com</a>. Even when swinging for the fences, sophomore slumps are nothing new, and that’s just about the best thing anyone can say about this ill-fated follow-up effort. At the end of the day, you have to wonder: did anyone really ask for a website for an email?</p>

        <p>And while we’re on the subject of the website itself…DIY efforts, like socially conscious punk offerings from the 1980s and Justin Vernon’s <i>For Emma, Forever Ago</i> (which was recorded in a rickety, frozen outhouse due to an Obama-era economic downturn) can hold the exclusive charm of the undiscovered, but this just feels lazy. The lack of bells, whistles, constant high-cost redesigns, and retrospective editing of content to revise previously published reviews according to current trends almost makes you think that these guys are coding the website themselves. </p>

        <p>The more you read, however, the more you wonder whether the website even matters. To continue the clever thread metaphor which itself runs throughout this review, no amount of beautiful cloth could dress up the fact that this is really just the same old tired content from the first year of the WMT. At the end of the day, it’s hard to escape the fact that to misattribute a quote is to miss an opportunity to demonstrate your knowledge as a music reviewer and intellectual. And those pseudonymous reviewers? Is there really any point to putting down someone else’s art if you’re not simultaneously exalting yourself? </p>

        <p>In the end, it’s hard to feel that <a href="www.weeklymusicthread.com">www.weeklymusicthread.com</a> earns its keep in the already crowded space of snobby online content about music. (At times, in fact, the WMT veers dangerously close to descending into nothing more than joyful musings about music that its contributors like, without any broader context or sociopolitical commentary to speak of.) Like the loudmouth uncle your mother insists on inviting over from Thanksgiving, you mostly just want these guys to shut up and stop sharing an opinion that nobody asked for, so that you can go back to sharing yours.</p>



