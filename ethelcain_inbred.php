          <p>Well, <i>Inbred</i> is finally here. The first thing you notice is, of course, the name, and the second is the album art: an image of two Ethels Cain in pious garb, flanking an out-of-focus crucifix in the background. One of the two has her hair down, her cheeks and jaw slightly fuller, her eyes slightly less angry and shadowed. The other is the opposite in each of those ways. Good and evil. Virtue and sin. Beauty and the beast. And they look almost exactly the same. Does <i>that</i> tell you anything about Cain’s take on the world around her?</p>

          <p>Speaking of that world, <i>Inbred</i> is the perfect name for one of her records. It evokes the subtle misery of her Southern Gothic settings and subject material, like if <i>Absalom, Absalom!</i> took place in a burned out, status post-subprime mortgage crisis strip mall, the butt of some coastal joke. The album opens with two previously released tracks, “Michelle Pfeiffer” and “Crush.” Since they’ve been out for a while—and since we dedicated <a href="https://weeklymusicthread.com/2021-03-22.php">a lot</a> of space to “Crush” in the past—we’ll refrain from writing further on them here, but they’re a worthy opening to the album, to say the least.</p>

          <p>After “Crush” comes “God’s Country,” which features Wicca Phase Springs Eternal. Similar to opener “Michelle Pfeiffer,” this track mixes Cain’s Gregorian chant-inspired raptures with emo rap courtesy of the Das Racist and Blink 182-inspired WPSE. At over eight minutes long, it’s the longest track we’ve gotten from Cain, but you barely notice its runtime. Maybe that’s because of the epic, timeless quality of a lot of Cain’s music, or maybe it’s because of what happens at the end of “God’s Country.” The song flickers like a candle, threatening to go out, before igniting again—but this time, it’s seemingly without any of the effects that usually mark Cain’s music. Her voice, and the instruments that surround it, sound perfectly organic (at least in comparison to the rest of the record). Fans of Cain’s prior work will sit up straight the first time they hear it—it’s that noticeable.</p>

          <p>“Unpunishable” opens, with the same vocal effects and slowly stirring bass line, like a coda to “Crush.” But instead of breaking into a sexy, dripping lust song, it stirs and simmers in desperation before finally breaking into a demonic, face-melting guitar outro. Title track “Inbred” builds in similar fashion, and the record finally ends on “Two-Headed Mother.” Clocking in at over six minutes in length, this is another extended affair. It opens with an interesting, meandering, melancholy, vaguely exotic guitar lament before Cain eventually enters with her trademark prayer-call wails. The guitar starts to groan and growl until, at about two and a half minutes in, the intro slumps to a close and Cain half-mumbles words of misery through thick vocal effects against a lazy, begrudging back beat. And just when you think that the entire album is going to whimper to a close, the girl and the guitar return, building to an epic and timeless sounding outro. Finally, the song fades out, flickering and smoldering like some sort of wreckage. Or maybe it backs out, a camera pulling out and away from the mess, letting you know that, even though you made it out, everything that you saw while you were there is still there, and always will be.</p>

          <p>Cain has said that <i>Inbred</i> was scary, that it was an effort to step outside of her comfort zone. In doing so, however, she’s proven what we wrote about a few weeks ago: that subtle re-inventions make or break sophomore efforts. Too similar, and listeners will see you as a one-trick pony. Too different, and you’ll lose the following that you had. We feel strongly that Cain has struck the perfect note with this one. On some level, she comes off as an artist who’s starting to hit her stride. And that’s exciting, because as good as <i>Golden Age</i> and <i>Inbred</i> are, it makes us think that the best is yet to come.</p>

          <p>So keep taking chances and making music, Ethel Cain. As we said in the introduction to this week’s post, it’s kind of mind-blowing to realize that a speaker of a foreign language can never hear their own accent. Well, a musician can never really hear their own music, either. Sure, we suppose, they can listen to it, but they can never <i>hear</i> it—not in the way that somebody else can. So this is just to let you know: we hear your music. Our friends and our boyfriends and our girlfriends hear your music. We listen to it alone and we listen to it together. And we talk about it. Like, a lot. For whatever that’s worth. </p>

